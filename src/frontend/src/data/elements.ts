export const getElementById = (z: number): ChemicalElement | undefined =>
  ELEMENTS.find((e) => e.z === z);

export const getElementsByCategory = (cat: string): ChemicalElement[] =>
  ELEMENTS.filter((e) => e.category === cat);

export const getPropertyRange = (
  elements: ChemicalElement[],
  prop: keyof ChemicalElement,
): { min: number; max: number } => {
  const values = elements
    .map((e) => e[prop])
    .filter((v): v is number => typeof v === "number");
  return { min: Math.min(...values), max: Math.max(...values) };
};
